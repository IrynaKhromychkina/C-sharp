namespace Calculator
{
    class Calculator
    {
        public static dynamic Addition(int firstNumber, int secondNumber) => firstNumber + secondNumber;
        public static dynamic Subtraction(int firstNumber, int secondNumber) => firstNumber - secondNumber;
        public static dynamic Multiplication(int firstNumber, int secondNumber) => firstNumber * secondNumber;
        public static dynamic Division(int firstNumber, int secondNumber) => firstNumber / (double)secondNumber;
    }
}
